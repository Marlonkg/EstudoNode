
let calculadora = {
    soma: (a,b) =>{
        return a + b
    },
    sub: (a,b) =>{
        return a - b
    },
    mult: (a,b) =>{
        return a * b
    },
    div: (a,b) =>{
        return a / b
    }
}

console.log(calculadora.soma(3,3))
console.log(calculadora.sub(4,3))
console.log(calculadora.mult(3,3))
console.log(calculadora.div(15, 3))