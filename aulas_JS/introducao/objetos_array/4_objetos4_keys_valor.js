//chaves ou keys são os nomes dados ao valor dentro do objeto, valor já fica explicito qual é...
let obj = {
    key1 : 'valor1',
    key2 : 'valor2',
    key3 : 'valor3',
    key4 : 'valor4',
}

//imprime apenas as chaves do objeto
console.log(Object.keys(obj))
//imprime apenas os valores sem chaves.
console.log(Object.values(obj))